'''
Escreva m programa que verique se uma letra é vogal u consoante.
'''
letra = input('Digite a letra para verificar se é vogal ou consoante:')

if letra == 'a' or letra == 'e' or letra == 'i' or letra == 'o' or letra == 'u':
    print('É vogal')
else:
    print('É consoante')


